public class While {
    public static void main(String[] args) {
        System.out.println("Loop start");

        int index = 1;
        while (index <= 10) {
            System.out.println("loop begin");
            System.out.println(index);
            index++;

            System.out.println("loop completed");
        }
        System.out.println("loop end");
    }


}
